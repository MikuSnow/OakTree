package io.github.redstoneparadox.oaktree.client.gui.util;

public class ScreenRect {
    float x;
    float y;
    float width;
    float height;

    public ScreenRect(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
